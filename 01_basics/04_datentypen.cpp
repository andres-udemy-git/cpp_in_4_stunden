#include <iostream>

int main() {
    int ganzzahl = 42;
    double kommazahl = 0.42;
    char buchstabe = 'c' + 2;
    bool flag = true;
    std::string text = "somewhat";

    std::cout << "Ganzzahl: " << ganzzahl << std::endl;
    std::cout << "Kommazahl: " << kommazahl << std::endl;
    std::cout << "Buchstabe: " << buchstabe << std::endl;
    std::cout << "Boolean: " << flag << std::endl;
    std::cout << "String: " << text << std::endl;
}