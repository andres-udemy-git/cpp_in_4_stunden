#include <iostream>
#include <math.h>

using namespace std;

int main() {
    cout << "----- Funktionen -----" << endl << endl;
    cout << "Betrag einer Zahl (abs(-3)): " << abs(-3) << endl;
    cout << "Wurzel von 42: " << sqrt(42) << endl;
    cout << "16 ^ 2 : " << pow(16 , 2) << endl;
    cout << "e: exp(1): " << exp(1) << endl;
    cout << "log(exp(1)): " << log(exp(1)) << endl;
}